    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        // dd($request->all());
        $request->validate([
            'first_name' => 'requried|string|max:255',
            'last_name' => 'requried|string|max:255',
            'email' => 'requried|string|max:255',
            'phone' => 'requried|string|max:255',
            'password' => 'requried|string|max:255',
        ]);

        $user = User::create([
            'first_name' => $request->first_name,
            'last_name' => $request->last_name,
            'email' => $request->email,
            'phone' => $request->phone,
            'password' => bcrypt($request->password)
        ]);
        if (!$user) {
            return redirect()->back()->with('errors', 'your form is Not Submit');
        }
        return redirect()->back()->with('success', 'your form is Submit');
    }

